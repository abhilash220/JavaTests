package interfaces;
public interface PuzzleInterface {
    /**
     *Abstract method to swap the values of two variables using a 3rd variable
     * @throws Exception
     */
    void swapVariableMethod() throws Exception;

    /**
     * Abstract method to swap the values of two variables without a 3rd variable
     * @throws Exception
     */
    void swapNoVariableMethod() throws Exception;
}
